const express = require('express')
const router = express.Router()
//modelName
const ttcVideo = require('../Models/ttcVideo')

const valiDateRouteFun = require('./valiDateRouteFun')
valiDateRouteFun(router,ttcVideo)

router.post('/create', async (req, res) => {
    try {
        const temp = await new ttcVideo(req.body)
        const response = await temp.save();
        console.log(response);
        return res.status(200).json(response);
    } catch (err) {
        return res.status(500).json({ error: err })
    }
});

router.post('/update/:id', async (req, res) => {
    try {
        const response = await ttcVideo.findByIdAndUpdate(req.params.id, req.body);
        return res.status(200).json(response)
    } catch (err) {
        return res.status(500).json({ error: err })
    }
})


router.delete('/delete/:id', async (req, res) => {
    try {
        const response = await ttcVideo.findByIdAndDelete(req.params.id);
        return res.status(200).json(response)
    } catch (err) {
        return res.status(500).json({ error: err })
    }
})

module.exports = router